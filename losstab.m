% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
load template pattern
load ('template.mat');
feature = importdata('feature.dat');

% /
global loss
edhandles = [];
edhandles = findobj(gcf,'Style','edit');
res = get(edhandles, 'String');
res = flipud(res);
res = reshape(res,10,10);
res = str2double(res);
loss = [res(:,[1:6]),res(:,10),res(:,7:9)];
loss = loss';

% 
x = zeros(1,25);    %
xmeans = zeros(1,25);   %
S = zeros(25,25);   %
S_ =zeros(25,25);   %S
p = zeros(1,10);    %
pw = zeros(1,10);    %
hx = zeros(1,10);   %
mode = [];
N = 0;
t = zeros(1,25);
% 
for i = 1:10
    N = pattern(i).num;  %
end
for i = 1:10
    pw(i) = pattern(i).num/N;
end

for n = 1:10
    pnum = pattern(n).num;
    %
    for k = 1:pnum
        for i = 1:25
            if pattern(n).feature(k,i)>0.3
                xmeans(i) = xmeans(i)+1;
            else
                xmeans(i) = xmeans(i)+0;
            end
        end
    end
    for i = 1:25
        xmeans(i) = xmeans(i)/pnum;
    end
    %
    for i = 1:pnum
        for j = 1:25
            if pattern(n).feature(i,j)>0.3
                mode(i,j) = 1;
            else
                mode(i,j) = 0;
            end
        end
    end
    for i = 1:25
        for j = 1:25
            s = 0;
            for k = 1:pnum
                s = s+(mode(k,i)-xmeans(i))*(mode(k,j)-xmeans(j)); 
            end
            s = s/(pnum - 1);
            S(i,j) = s;
        end
    end
    %S
    S_ = pinv(S);   %pinv
    dets = det(S);  %det
    % 
    for i = 1:25
        if feature(i)>0.3
            x(i) = 1;
        else
            x(i) = 0;
        end
    end
    for i = 1:25
        x(i) = x(i) - xmeans(i);
    end
    t = x*S_;
    t1 = t*x';
    t2 = log(pw(n));
    t3 = log(dets+1);
    p(n) = -t1/2+t2-t3/2;
end

for n = 1:10
    tem = 0;
    for i = 1:10
        tem = tem+loss(n,i)*p(i);
    end
    hx(n) = tem;
end
[tem,num] = max(hx);    %
num = num-1;

set(handles.text24,'string',num2str(hx(1)));
set(handles.text25,'string',num2str(hx(2)));
set(handles.text26,'string',num2str(hx(3)));
set(handles.text27,'string',num2str(hx(4)));
set(handles.text28,'string',num2str(hx(5)));
set(handles.text29,'string',num2str(hx(6)));
set(handles.text30,'string',num2str(hx(7)));
set(handles.text31,'string',num2str(hx(8)));
set(handles.text32,'string',num2str(hx(9)));
set(handles.text33,'string',num2str(hx(10)));

% 
set(gcf,'visible','off');

h = figure(homework);
set(h,'visible','on');

%
str = num2str(num);
str = ['Bayes' str];
msgbox(str,'');


